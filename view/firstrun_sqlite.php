<h3>Enable Caching</h3>
    <p>
    This one is <strong>VERY</strong> important. Unless your GEDCOM file is 
    very small (100ish people) your site will be slow, unless you enable the cache.
    </p>
    <p>
    Change the permissions of the <em>/cache/</em> directory to be writable by 
    the web server.
    </p>

